const calc = {
	addition(a, b) {
		return (a + b)
	},
	subtraction(a, b) {
		return (a - b)
	},
	multiplication(a, b) {
		return (a * b)
	},
	division(a, b) {
		return (a / b)
	},
}

console.log(calc.addition(5, 6));
console.log(calc.subtraction(5, 6));
console.log(calc.multiplication(5, 7));
console.log(calc.division(5, 2));
